    

public class Type_of_Expression_and_Increment_5 {
    public static void main(String[] args) {
        //increment and decrement
        int i =56;
        System.out.println(i++);
        System.out.println(i);
        System.out.println(++i);
        System.out.println(i);


        //quiz
        int x = 7;
        int y = (++x) * 8;
        System.out.println(y);

        char ch = 'b';
        ++ch;
        System.out.println(ch);

    }}
